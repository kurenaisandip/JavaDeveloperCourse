public class DataTypes {
    public static void main(String[] args)
    {
        // creating Integer variables
        int a = 10;
        short b = 99;
        long c = 1234567890123l;
        byte d = 1;


        // Printing variable
        System.out.println("a = " +a);
        System.out.println("b = " +b);
        System.out.println("c = " +c);
        System.out.println("d = " +d);
    }
}
